# 用户价值等级分类（单位：钻石）
USER_VALUE_TIERS = {
    "super_vip": 10000,      # 超级大哥：>=10000钻石
    "vip": 5000,             # VIP用户：>=5000钻石
    "high_value": 1000,      # 高价值：>=1000钻石
    "medium_value": 100,     # 中等价值：>=100钻石
    "normal": 0,             # 普通用户：<100钻石
}


def get_gift_value(gift_name: str) -> int:
    """
    获取礼物价值（钻石数）
    """
    return GIFT_PRICES.get(gift_name, 1)  # 默认1钻石


def get_gift_yuan_value(gift_name: str) -> float:
    """
    获取礼物价值（人民币）
    1钻石 ≈ 0.1元
    """
    diamonds = get_gift_value(gift_name)
    return diamonds * 0.1


def classify_user_value(total_gift_value: int) -> dict:
    """
    根据用户送礼总价值分类用户等级
    
    Args:
        total_gift_value: 用户送礼总价值（钻石）
    
    Returns:
        {
            "tier": "super_vip" | "vip" | "high_value" | "medium_value" | "normal",
            "label": "超级大哥" | "VIP用户" | "高价值用户" | "活跃用户" | "普通用户",
            "color": "#ff0000" | "#ff6600" | "#9333ea" | "#3b82f6" | "#6b7280",
            "icon": "👑" | "💎" | "⭐" | "🌟" | "",
            "total_value": 钻石数,
            "yuan_value": 人民币金额
        }
    """
    tier = "normal"
    label = "普通用户"
    color = "#6b7280"
    icon = ""
    
    if total_gift_value >= USER_VALUE_TIERS["super_vip"]:
        tier = "super_vip"
        label = "超级大哥"
        color = "#ff0000"
        icon = "👑"
    elif total_gift_value >= USER_VALUE_TIERS["vip"]:
        tier = "vip"
        label = "VIP用户"
        color = "#ff6600"
        icon = "💎"
    elif total_gift_value >= USER_VALUE_TIERS["high_value"]:
        tier = "high_value"
        label = "高价值用户"
        color = "#9333ea"
        icon = "⭐"
    elif total_gift_value >= USER_VALUE_TIERS["medium_value"]:
        tier = "medium_value"
        label = "活跃用户"
        color = "#3b82f6"
        icon = "🌟"
    
    return {
        "tier": tier,
        "label": label,
        "color": color,
        "icon": icon,
        "total_value": total_gift_value,
        "yuan_value": round(total_gift_value * 0.1, 2),
    }
